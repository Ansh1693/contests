    for (long long i = 1; i < m; ++i)
    {
        prod[i] = (i * prod[i - 1]) % mod;
    }
    int tc = 1;
    cin >> tc;
    for (int t = 1; t <= tc; t++)
    {
        // cout << "Case #" << t << ": ";
        solve(prod);
    }
