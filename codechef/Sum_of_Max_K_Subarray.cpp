const int mod = 1e9 + 7;
const int m = 1e6;

void solve(vi &prod)
{
    int n;
    cin >> n;
    for (int i = 1; i <= n; ++i)
    {
        long long a = n / i;
        long long b = (i - 1) + (i - 1) * a + a;
        long long c = b - n;
        long long ans = (prod[a] * prod[n - a]) % mod;
        ans = (ans * (c + 1)) % mod;
        cout << ans << " ";
    }
    cout << nL;
}
